export const MAX_KEY_LENGTH: number = Math.max(...[...ROMAJI_TABLE.keys()].map((k) => k.length));

/** テーブルキーの真の接頭辞集合。末尾の打鍵途中判定（pending）に使う */
export const KEY_PREFIXES: ReadonlySet<string> = (() => {
  const prefixes = new Set<string>();
  for (const key of ROMAJI_TABLE.keys()) {
    for (let len = 1; len < key.length; len++) {
      prefixes.add(key.slice(0, len));
    }
  }
  return prefixes;
})();
